                <p>On Saturday, November 8, 2008, the third alumni chapter of Alpha Phi Alpha Fraternity, Inc., in the Baltimore metropolitan area, Rho Tau Lambda, became official with a Chartering Ceremony and Celebration held at the Huber Memorial Church in East Baltimore.
In the spring of 2006, Brothers Samuel Brown, Robert Dashiell and Vincent Whitmore discussed the possibility of creating a new chapter of Alpha Phi Alpha in Baltimore. They wanted to reclaim inactive members who wanted to rekindle their brotherhood ties. Additionally, they wanted to attract brothers who would recommit to strengthening the community through outreach, leadership and public service. They were soon joined by J. Wayman Henry who helped spearhead the process by utilizing his influence and knowledge among like minded brothers in Maryland and in other states and thereby helped to populate the process. The numbers grew to twenty-one brothers who met at the Alpha Phi Alpha Baltimore national office on August 23, 2006 to begin the journey.</p>
				<div class="image-holder"><img class="image-2" src="images/history/rtl-history-3.jpg" /></div>
                <p>Robert Dashiell was elected Committee Chair and he joyfully accepted the task of leading the brothers through the uninhabited waters of the chartering process. This successful endeavor would become a first in the history of Alpha Phi Alpha. There was no how to do protocol, no script to follow. It was designed by "Alpha Man Law".</p>
                <img class="image-3 right" src="images/history/rtl-history-1.jpg" />
                <p>Mutual love for the fraternity provided the needed inspiration and guidance toward the fulfillment of our shared dream. Samuel Brown, Program Coordinator facilitated the growth and development of the group which initially adopted the unofficial name, Kappa Kappa Lambda then later, and more appropriately the "Alpha Renaissance Committee". The Committee met regularly to design plans for the chartering of a new alumni chapter of Alpha Phi Alpha, Fraternity, Inc. in Baltimore, Maryland. Foremost among the many completed tasks of the committee was the prototype Business Plan submitted to the General President and Members of the Board of Directors of Alpha Phi Alpha, Fraternity, Inc.</p>
